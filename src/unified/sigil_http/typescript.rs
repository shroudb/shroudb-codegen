//! TypeScript HTTP REST SDK generator.

use super::HttpSdkGenerator;
use crate::generator::GeneratedFile;
use crate::unified::ir::{CommandIR, EngineIR, HttpEndpointIR, UnifiedIR};
use heck::{ToLowerCamelCase, ToPascalCase};
use std::fmt::Write;

pub struct TsHttpGenerator;

impl HttpSdkGenerator for TsHttpGenerator {
    fn language(&self) -> &'static str {
        "TypeScript"
    }

    fn generate(&self, engine: &EngineIR, ir: &UnifiedIR) -> Vec<GeneratedFile> {
        let pkg = &ir.packages.typescript;
        let pascal = engine.name.to_pascal_case();
        vec![
            gen_package_json(pkg, &ir.version, engine),
            gen_tsconfig(),
            gen_index(&pascal),
            gen_errors(engine),
            gen_types(engine),
            gen_client(engine, &pascal),
            gen_readme(pkg, engine, &pascal),
            gen_agents(pkg, engine, &pascal),
        ]
    }
}

