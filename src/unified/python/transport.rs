//! Transport interface, RESP3 codec, and HTTP transport generation for Python.

use crate::generator::GeneratedFile;
use crate::unified::ir::UnifiedIR;

pub(super) fn generate(ir: &UnifiedIR) -> Vec<GeneratedFile> {
    let pkg = &ir.packages.python;
    vec![
        gen_transport_init(pkg),
        gen_interface(pkg),
        gen_resp3(pkg),
        gen_http(pkg),
    ]
}

fn gen_transport_init(pkg: &str) -> GeneratedFile {
    GeneratedFile {
        path: format!("{pkg}/_transport/__init__.py"),
        content: "# Auto-generated by shroudb-codegen. Do not edit.\n".into(),
    }
}

